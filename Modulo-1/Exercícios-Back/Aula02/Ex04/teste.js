let lado = 5, largura = 16, comprimento = 6, base = 12, altura = 8, resposta = 0;

//quadrado
resposta = lado * lado;
console.log(`A area do quadrado é: ${resposta}`);

//retangulo
resposta = largura * comprimento;
console.log(`A area do retangulo é: ${resposta}`);

//triangulo
resposta = (base * altura) / 2;
console.log(`A area do triangulo é: ${resposta}`);